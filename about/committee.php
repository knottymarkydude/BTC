            <div id='third_window' class='modal_window'>
                <h3>Treasurers Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/></p>
                <p>Weekly Reconcile bank account and PayPal receipts against session registers. Update accounts spreadsheet and categorise payments & receipts (1hour)</p>
                <p>Adhoc payments from PayPal and transfer balance of funds to bank account (15 mins)</p>
                <p>Keep records of receipts and payments, invoices and expenses, make payments for invoices (15 mins)</p>
                <p>Make coaches payments, reconcile and pay balances (10mins)</p>
                <p>Update coaching session records and reconcile registers against payments received on attendance spreadsheet (1hour)</p>
                <p>Attend committee meeting every 6 weeks and produce figures for committee members on income and expenditure (15mins)</p>
                <p>Take cash payments and record petty cash income & expenditure. Reconcile cash tin, pay in cheques and cash at bank (30mins)</p>
                <p>Answer queries re sessions paid for</p>
                <p>Ad hoc refund re clothing and sessions</p>
                <p>Attend AGM & report on this year’s financial situation</p>
            </div>  

            <div id='forth_window' class='modal_window'>
                <h3>Membership Secretary Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>

                    The role is to encourage, explain, gain and maintain club members. Position roles/duties include: 
                    1. Respond to direct membership enquiries from club webmail, committee or members 
                    2. Support Social Media secretary on membership enquiries from any other source 
                    3. Ensure potential members are welcomed by committee members/coaches at trial/first event/session. 
                    4. When members join: 
                    4.1 send informal welcome by club email 
                    4.2 Send formal welcome from Mailchimp 
                    4.3 Inform Social Media secretary to invite to Facebook page 
                    4.4 Update membership spreadsheet 
                    4.5 Introduce to club with blog on club Facebook page 
                    4.6 Send membership card to new members 
                    4.7 Issue to committee/coaches/dropbox updated membership list on monthly basis 
                    5. Reconcile membership payments with Treasurer 
                    6. Manage annual membership renewals 
                    7. Manage affiliations of members to UKA 
                    It takes around an hour to complete a new membership so not onerous although can be longer if extended email exchanges to answer membership enquiries and present rate is 4-6 per month. 
                    The role takes a max 2-3 hours per week and often evenings or weekends, although the annual renewal is the biggest task and for 130+ members probably represents a week’s work spread out during December/January.
                </p>
            </div>  

            <div id='fifth_window' class='modal_window'>
                <h3>Social Media Officer Job Description</h3>
                <p>Responsibilities and Duties:
                    To maintain, monitor and refresh the public and private FB and Twitter pages, 
                    encourage support of members through the social media. Update with information, 
                    notices and ensure that members’ questions are responded to by someone appropriate. 
                    Add new members when they join. Field and be first response to email and private messenger 
                    enquiries. Port of call for members with questions and issues. I also send group emails 
                    out for mass communication, although this is may not seem relevant to the social media title. 
                    Time involved - difficult to pinpoint but is a time consuming role, members can and do make 
                    contact often and usually at weekends and evenings.
                </p>
            </div> 

            <div id='sixth_window' class='modal_window'>
                <h3>Press Officer Job Description</h3>
                <p>Responsibilities and Duties:
                    • Seek out story ideas from the club members on recent racing activities.
                    • Research the events and results.
                    • Obtain images and quotes of recent racing activities.
                    • Edit and manage the production and circulation of a weekly press 
                    release.
                    • Liaise with the Grantham Journal.
                    • Edit content, proof reading and revising draft copies.
                    • Purchase a weekly copy of the Grantham Journal and file the record.
                    Knowledge and Skills Required:
                    • Excellent written English. 
                    • Able to meet press deadlines. 
                    • Editing, proof reading and formatting skills.

                    The estimated time commitment required as the Press Officer is three hours per week.
                </p>
            </div>  

            <div id='seventh_window' class='modal_window'>
                <h3>Training Coordinator Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>
                    The role makes sure that we have coaches and leaders available for 
                    the sessions the club puts on. Towards the end of the month I send a 
                    spreadsheet out to all the coaches and run/ride leaders containing dates 
                    and times for the following months’ sessions. They will then respond with 
                    their availability and I try to match sessions with available coaches for the 
                    month so that the load is spread evenly and hopefully fairly between them all. 
                    I send the completed schedule back out so they all know what they are doing. 
                    On an ongoing basis I deal with any last minute changes that might occur due to 
                    coaches not being available for whatever reason. I write up the weekly “what’s on post” 
                    every Sunday afternoon. I also deal with the Meres for some of the pool bookings etc. 
                    In terms of time spent; it probably adds up to about 6-8 hours to get the monthly 
                    schedule sorted and perhaps between 1 and 2 hours on a weekly basis.
                </p>
            </div>  

            <div id='eighth_window' class='modal_window'>
                <h3>Welfare Officer Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>
                    The role entails offering help and guidance to any club member who has concerns arising within 
                    the club environment. This includes all coached sessions and any of our social events. 
                    The position of Welfare Officer and Assistant Welfare Officer need to be represented by 
                    both a Male and Female. This offers a fairer and more diplomatic view of an issue or problem. 
                    It will also offer club members a choice of Welfare Officers to approach and speak to based 
                    on what they feel most comfortable with. The Welfare Officers need to be: Approachable, 
                    sensitive, non-confrontational, good listeners with empathy, respect confidentiality, be 
                    tactful and a good communicator. Time involved: This is totally dependent on the issue or 
                    problem you are presented with. In addition, there is a commitment to the clubs committee 
                    meetings which are appx 2 hours and take place once a month. You will also be encouraged 
                    to attend training/information events to keep up to date on welfare/safeguarding issues.
                </p>
            </div>  

            <div id='ninth_window' class='modal_window'>
                <h3>Clothing Coordinator Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>
                    The role requires someone who is flexible, dedicated and trustworthy. The aim of the role is to 
                    continually communicate and supply club members with items of clothing whilst giving everyone a 
                    focal point of contact. Skills Required: Good attention to detail, patience of a angel, organised, 
                    forthright with a positive approach. Duties: Hold various amounts of kit for sizing, assist with 
                    correct size ordering, able to answer questions via social media, liaise with Club Treasurer ref 
                    payments, bank and account for cash received, check emails daily for orders/questions, arrange 
                    convenient times for fittings/sizing, deal with complaints, order replacement items and new stock, 
                    keep records (spreadsheets), have a sense of humour, regular updates of stock levels on Facebook. 
                    This is a time consuming role with varying amounts of input. Minimum input of 4/5 hours a week.
                </p>
            </div>  

            <div id='tenth_window' class='modal_window'>
                <h3>Events Coordinator Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>
                    Organise teams and entries for national relay championships. 
                    Set list of targeted events and promote within club to actively 
                    engage members and encourage a good turn-out, negotiate club discounted 
                    entry where possible. Arrange for club gazebo to be erected where appropriate.
                </p>
            </div>  

            <div id='eleventh_window' class='modal_window'>
                <h3>Social Coordinator Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>
                    The role includes organising and coordinating all club social events i.e. any social nights / days out; 
                    the two most important celebrations for the social sec are the two main events on the BTC calendar, 
                    the end of season presentation evening/meal and the Summer BBQ. You must be able to liaise with third 
                    party organisations and venues, have a good sense of humour and have a passion for the role. 
                    An important part of the Social Secretary roll is to listen to other members of the club and 
                    what they believe to be a good idea for an event, take the idea on and work with the member/s to make their ideas a success. 
                    The role takes an hour or so a week and is a perfect insight into how the club functions behind the scenes.
                </p>
            </div>  

            <div id='twelth_window' class='modal_window'>
                <h3>GO-Tri Coordinator Job Description</h3>
                <p>Responsibilities and Duties:<br/><br/>
                    This role requires someone who is confident and has the skill to organise a team of 
                    around 12 volunteers to host currently 4 events a year. Other duties include: meeting 
                    venue management to discuss their requirements such as costs and availability, selecting, 
                    measuring and recce of course, completion of Risk Assessment, promoting and marketing the 
                    event through social media, flyers and word of mouth. Ordering necessary equipment and 
                    essentials required for the event. 
                    Making sure adequate marshals are supplied and have covered child welfare issues. 
                    Publishing results to British Triathlon and posting on website. Some storage of equipment is 
                    required. Difficult to say how many hours spent each week on this role.
                </p>
