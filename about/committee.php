                <h3>Club Secretary Job Description</h3>
                <p>Responsibilities and Duties:</p>

                <ul>
                    <li>Prepare the agenda for club meetings in consultation with the Chairperson.</li>
                    <li>Send notice of the meetings.</li>
                    <li>Take the minutes of meetings.</li>
                    <li>Write up the minutes as soon as possible after the meeting.</li>
                    <li>After minutes are approved by Chairman, forward to webmaster.</li>
                    <li>Collate and arrange for the printing of the annual report. </li>
                </ul>

                <p>Knowledge and Skills Required:</p>

                <ul>
                    <li>Can communicate effectively</li>
                    <li>Is well organised</li>
                    <li>Can maintain confidentiality on relevant matters</li>
                    <li>Can take accurate minutes</li>
                </ul>
                <p>The estimated time commitment required as the Secretary of five hours per meeting (prior, during and post).</p>
