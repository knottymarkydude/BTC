                <h3>Chairmans Job Description</h3>
                <p>The role is very varied, ranging from official duties of chairing meetings, agreeing agendas, 
                    chasing matters outstanding and keeping a strategic position driving the club forwards. 
                    It also involves more ceremonial duties including some public speaking whilst presenting 
                    prizes at events and at the annual prize giving dinner. Other duties include resolving issues 
                    arising from the general membership and the committee; mediating, negotiating and being open 
                    and approachable whilst ensuring confidentiality. 
                    The role is quite time consuming and I would estimate that it takes in excess of 4hrs per week.</p>
